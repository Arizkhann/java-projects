-- number functions
-- abs(): return the absolute values
select abs(-12);

-- sqrt() : return sqrt values
select sqrt(49);

-- power(n,m) : return n power m values

select power(2,3);

-- ceiling() : returns the greatest integer values
select ceiling(12.0000001);

-- floor() : returns the smallest integer  values
select floor(12.999999);

-- round(): used to round number to particular digit specified
select round(8778.7876,3);
select round(8778.7876,2);
select round(8778.7876,1);
select round(8778.7876,0);
select round(8778.7876,-1);
select round(8778.7876,-2);
select round(8778.7876,-3);
